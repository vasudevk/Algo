package prep;

public class Party {

    String partyName;
    String leader;

    /**
     * Gets partyName
     *
     * @return value of partyName
     */
    public String getPartyName() {
        return partyName;
    }

    public void setPartyName(String partyName) {
        this.partyName = partyName;
    }

    /**
     * Gets leader
     *
     * @return value of leader
     */
    public String getLeader() {
        return leader;
    }

    public void setLeader(String leader) {
        this.leader = leader;
    }
}
